function exercicio14(){
    alert("Dizer se é maior ou menor que 10.")
    let numero = parseInt(prompt("Digite um número: "))

    if (numero>10){
        alert("Esse número é maior que 10!")
    }
    else if (numero == 10){
        alert("Esse número é igual a 10!")
    }
    else{
        alert("Esse número é menor que 10!")
    }
}